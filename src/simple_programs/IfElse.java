package simple_programs;

public class IfElse {

    public static void main(String[] args) {

        int num = 2;        //initializing num variable
        if(num==2)          //if statement will execute if condition is true
            System.out.println("if statement executed");
        else                //else statement will execute if condition is false
            System.out.println("else statement executed");
    }
}
